#include "get_next_line.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && ((unsigned char *)s)[i] != (unsigned char)c)
		i++;
	if (i < n && ((unsigned char *)s)[i] == (unsigned char)c)
		return ((void *)s + i);
	return (NULL);
}
